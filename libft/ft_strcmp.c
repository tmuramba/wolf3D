#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int				i;
	unsigned char	*b;
	unsigned char	*a;

	i = 0;
	b = (unsigned char *)s2;
	a = (unsigned char *)s1;
	while (a[i] || b[i])
	{
		if (a[i] != b[i])
		{
			return (a[i] - b[i]);
		}
		i++;
	}
	return (0);
}
